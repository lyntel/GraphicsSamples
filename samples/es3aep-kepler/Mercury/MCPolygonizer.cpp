MCPolygonizer::MCPolygonizer(float boxSize, float cellSize, const char* shaderPrefix):
	m_cellSize(cellSize),
	m_updateProg(0),
	m_shaderPrefix(shaderPrefix) {
	m_cellsDim = static_cast<size_t>(boxSize / cellSize);	

	m_indices = new ShaderBuffer<uint32_t>(m_cellsDim * m_cellsDim * m_cellsDim * 3);

	// Initialize indices
	uint32_t* indices = m_indices->map();
	for (unsigned int i = 0; i < m_cellsDim * m_cellsDim * m_cellsDim * 3; ++i) {
		indices[i] = i;
	}
	m_indices->unmap();

	m_triTable = new ShaderBuffer<int>(4096);

	// Initialize triangle table buffer
	int* tTable = m_triTable->map();
	for (int i = 0; i < 4096; ++i) {
		tTable[i] = triTable[i];
	}
	m_triTable->unmap();


	// Create compute program for computing isovalues
	ShaderPipeline::loadShaders(m_programPipeline, m_updateProg, m_shaderPrefix, "shaders/cubesCS.glsl");

	//TODO: This buffer size can be reduced as all 8 positions don't need to be stored. Do same in CS as GS
    m_pos = new ShaderBuffer<vec4f>(m_cellsDim*m_cellsDim*m_cellsDim * 8, GL_DYNAMIC_READ);
    m_packedPosVal = new ShaderBuffer<vec4f>(m_cellsDim*m_cellsDim*m_cellsDim * 3, GL_DYNAMIC_DRAW);
    m_val = new ShaderBuffer<float>(m_cellsDim*m_cellsDim*m_cellsDim * 8 * 4, GL_DYNAMIC_READ);

	m_startPos = vec4f(-floor((m_cellsDim * m_cellSize) / 2.0f),
		-floor((m_cellsDim * m_cellSize) / 2.0f),
		-floor((m_cellsDim * m_cellSize) / 2.0f), 1.0f);

}


void MCPolygonizer::generateMesh(ShaderBuffer<vec4f>* positions, ShaderBuffer<vec4f>* velocities, float timeDelta) {

	// For compute shader
	glBindProgramPipeline(m_programPipeline);
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 3, m_pos->getBuffer());     // cell vertices positions
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 4, m_val->getBuffer());     // cell vertex isovalues
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 5, positions->getBuffer()); // particle positions
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 6, velocities->getBuffer());// particle velocites

	const size_t numVerticesPerCell = 8;

	GLuint loc = glGetUniformLocation(m_updateProg, "startPos");
	glProgramUniform4fv(m_updateProg, loc, 1, &m_startPos[0]);

	loc = glGetUniformLocation(m_updateProg, "cellsDim");
	glProgramUniform1ui(m_updateProg, loc, m_cellsDim);

	loc = glGetUniformLocation(m_updateProg, "cellSize");
	glProgramUniform1f(m_updateProg, loc, m_cellSize);

	loc = glGetUniformLocation(m_updateProg, "timeStep");
	glProgramUniform1f(m_updateProg, loc, timeDelta);
	
	glDispatchCompute(m_cellsDim * numVerticesPerCell, m_cellsDim, m_cellsDim);
	
    glMemoryBarrier(GL_SHADER_STORAGE_BARRIER_BIT);
    glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 6, 0);
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 5, 0);
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 4, 0);
	glBindBufferBase(GL_SHADER_STORAGE_BUFFER, 3, 0);
	
	glBindProgramPipeline(0);


	//Update separate pos buffers for rendering pipeline

	vec4f* pos = m_pos->map(GL_MAP_READ_BIT);
	float* val = m_val->map(GL_MAP_READ_BIT);
	vec4f* packedPosVal = m_packedPosVal->map();

	//TODO: This operation can be done in the Compute Shader
	for (unsigned int i = 0; i < m_cellsDim * m_cellsDim * m_cellsDim; ++i) {
		// First index of triangle stores position of first vertex of cube cell
		packedPosVal[i * 3 + 0] = vec4f(pos[8 * i + 0].x, pos[8 * i + 0].y, pos[8 * i + 0].z, 1.0f);
		
		// Second and Third indices store isovalues 0,1,2,3 and 4,5,6,7 respectively
		packedPosVal[i * 3 + 1] = vec4f(val[32 * i + 0], val[32 * i + 4], val[32 * i + 8], val[32 * i + 12]);
		packedPosVal[i * 3 + 2] = vec4f(val[32 * i + 16], val[32 * i + 20], val[32 * i + 24], val[32 * i + 28]);
	}


	m_pos->unmap();
	m_val->unmap();
	m_packedPosVal->unmap();
}

